// GIT COMANDOS

/* $ git init */
/* Inicia um repositório */

/* $ git add style.css */
/* Adiciona o arquivo style.css ao index do git. Com o $ git add -A,
adicionamos todos os arquivos. */

/* $ git status */
/* Mostra os arquivos que tiveram mudanças. */

